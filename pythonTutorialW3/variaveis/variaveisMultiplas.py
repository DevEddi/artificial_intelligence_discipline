#Podemos declarar variaveis em uma mesma linha

x, y, z = "Orange", "Apple", "Pineapple"

print(x)
print(y)
print(z)

print(type(x))
print(type(y))
print(type(z))

#Podemos declarar um valor para varias variaveis

x = y = z = "Estou aqui, ali e aqui"

print(x)
print(y)
print(z)

print(type(x))
print(type(y))
print(type(z))

#Descompacte uma lista

fruits = ["apple", "banana", "cherry"]

x, y, z = fruits

print(x)
print(y)
print(z)
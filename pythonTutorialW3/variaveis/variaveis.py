#O Python, nao necessita  de declarar uma variavel, no momento que atribui o valor na variavel, ele ja reconhece.

x = 5
y = "Nao precisa declarar"

print(x)
print(y)

#Mas se caso eu precisar saber o tipo da variavel, basta apenas colocar 'type()'

x = 120
y = "The Python"
z = 2.0

print(type(x))
print(type(y))
print(type(z))


#Strings podemos declarar com aspas simples e aspas duplas

x = "Vanessa"
print(x)
u = 'Brenda'
print(u)

#Nomes de variaveis, tem diferenca sendo minuscula e maiuscula , entao preste atenca na hora declarar

